func Test_compareStrs(t *testing.T) {
	type args struct {
		as []string
		bs []string
	}
	var tests = []struct {
		name string
		args args
		want int
	}{
		{
			name: "1",
			args: args{[]string{"t", "1-10.html"}, []string{"a", "3-67.html"}},
			want: 1,
		},
		{
			name: "2",
			args: args{[]string{"t", "12-10.html"}, []string{"t", "3-67.html"}},
			want: 1,
		},
		{
			name: "3",
			args: args{[]string{"list", "12-10.html"}, []string{"t", "3-67.html"}},
			want: 1,
		},
		{
			name: "4",
			args: args{[]string{"list"}, []string{"t", "3-67.html"}},
			want: -1,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := compareStrs(tt.args.as, tt.args.bs); got != tt.want {
				t.Errorf("compareStrs() = %v, want %v", got, tt.want)
			}
		})
	}
}

